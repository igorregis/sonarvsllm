/**
 * Calcula a área de um triângulo isósceles.
 *
 * @param base A base do triângulo.
 * @param altura A altura do triângulo.
 * @return A área do triângulo.
 */
public static double calcularAreaTrianguloIsosceles(double base, double altura) {
    // Calcula a área usando a fórmula: área = (base * altura) / 2
    double area = (base * altura) / 2;
    // Retorna o resultado do cálculo da área
    return area;
}